#!/usr/bin/env node
/**
 * Fetch and build the Tatoeba tok↔eng sentence-pair corpus.
 *
 * Downloads the three TSVs from Tatoeba, joins them, and writes a compact
 * JSON array of { tp, en } pairs to src/content/corpus/tatoeba.json.
 *
 * License: Tatoeba content is CC BY 2.0 FR. Attribution is recorded in the
 * header file src/content/corpus/LICENSE.md.
 */
import { mkdirSync, writeFileSync, existsSync, createReadStream } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createInterface } from 'node:readline';
import { spawnSync } from 'node:child_process';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const cacheDir = resolve(root, '.tatoeba-cache');
const outDir = resolve(root, 'src/content/corpus');
mkdirSync(cacheDir, { recursive: true });
mkdirSync(outDir, { recursive: true });

const urls = {
  tok: 'https://downloads.tatoeba.org/exports/per_language/tok/tok_sentences.tsv.bz2',
  eng: 'https://downloads.tatoeba.org/exports/per_language/eng/eng_sentences_detailed.tsv.bz2',
  links: 'https://downloads.tatoeba.org/exports/per_language/tok/tok-eng_links.tsv.bz2',
};

async function downloadAndExtract(url, cachePath) {
  const bz2Path = `${cachePath}.bz2`;
  if (existsSync(cachePath)) return cachePath;
  if (!existsSync(bz2Path)) {
    console.log(`fetching ${url}`);
    const res = await fetch(url);
    if (!res.ok) throw new Error(`${url}: ${res.status}`);
    const buf = Buffer.from(await res.arrayBuffer());
    writeFileSync(bz2Path, buf);
  }
  console.log(`decompressing ${bz2Path}`);
  const r = spawnSync('bunzip2', ['-k', bz2Path], { stdio: 'inherit' });
  if (r.status !== 0) throw new Error('bunzip2 failed');
  return cachePath;
}

async function readTsv(path, handler) {
  const rl = createInterface({ input: createReadStream(path), crlfDelay: Infinity });
  for await (const line of rl) handler(line);
}

async function main() {
  const tokPath = await downloadAndExtract(urls.tok, resolve(cacheDir, 'tok_sentences.tsv'));
  const engPath = await downloadAndExtract(urls.eng, resolve(cacheDir, 'eng_sentences_detailed.tsv'));
  const linksPath = await downloadAndExtract(urls.links, resolve(cacheDir, 'tok-eng_links.tsv'));

  // Load tok sentences id → text
  const tokById = new Map();
  await readTsv(tokPath, (line) => {
    const [id, , text] = line.split('\t');
    if (id && text) tokById.set(id, text);
  });
  console.log(`loaded ${tokById.size} TP sentences`);

  // Load links: tok_id → eng_id[]
  const engIdsByTok = new Map();
  await readTsv(linksPath, (line) => {
    const [tok, eng] = line.split('\t');
    if (!tok || !eng) return;
    const arr = engIdsByTok.get(tok) ?? [];
    arr.push(eng);
    engIdsByTok.set(tok, arr);
  });
  console.log(`loaded ${engIdsByTok.size} TP ids with English links`);

  // Collect all referenced English IDs
  const neededEng = new Set();
  for (const engIds of engIdsByTok.values()) for (const id of engIds) neededEng.add(id);

  // Load ONLY referenced English sentences (keeps memory bounded)
  const engById = new Map();
  await readTsv(engPath, (line) => {
    const parts = line.split('\t');
    const id = parts[0];
    const text = parts[2];
    if (id && text && neededEng.has(id)) engById.set(id, text);
  });
  console.log(`matched ${engById.size} English sentences`);

  // Build pairs — one TP sentence may have multiple English translations; pick the shortest.
  const pairs = [];
  for (const [tokId, engIds] of engIdsByTok) {
    const tp = tokById.get(tokId);
    if (!tp) continue;
    let best = null;
    for (const engId of engIds) {
      const en = engById.get(engId);
      if (!en) continue;
      if (!best || en.length < best.length) best = en;
    }
    if (best) pairs.push({ tp, en: best });
  }

  // Sort deterministically for reproducible builds
  pairs.sort((a, b) => a.tp.localeCompare(b.tp));

  writeFileSync(resolve(outDir, 'tatoeba.json'), JSON.stringify(pairs, null, 0) + '\n');
  console.log(`wrote ${pairs.length} bilingual pairs to src/content/corpus/tatoeba.json`);

  // License file
  const license = `# Tatoeba Corpus — Attribution

Toki Pona and English sentence pairs in this corpus are sourced from
[Tatoeba.org](https://tatoeba.org/), a collaborative multilingual sentence
database.

Content is licensed under [CC BY 2.0 FR](https://creativecommons.org/licenses/by/2.0/fr/).
Attribution: individual Tatoeba contributors.

This file was generated by scripts/fetch-tatoeba-corpus.mjs on ${new Date().toISOString()}.
Pairs: ${pairs.length}.
`;
  writeFileSync(resolve(outDir, 'LICENSE.md'), license);
}

main().catch((e) => { console.error(e); process.exit(1); });
